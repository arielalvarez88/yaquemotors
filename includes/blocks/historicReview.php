    <h2>Rese&ntilde;a Hist&oacute;rica</h2>
    <p>
        En el año 1968, a  los señores Tomás Suárez Tamargo y Giuseppe de Augustino, se les presenta la oportunidad de introducirse en la industria automotriz a través de la comercialización de vehículos. Ese mismo año, ellos deciden constituir la empresa el yaque Motors, ubicándola en un terreno de  3,000 metros cuadrados en la Avenida 27 de Febrero de la ciudad de Santiago de los Caballeros.

        
        <br/>
        <br/>
        
        Desde sus inicios, la empresa es dealer de Santo Domingo Motors Company, representando las marcas de vehículos Nissan y General Motors. Debido al rápido incremento en las ventas, la empresa se ve en la necesidad de ofrecer un servicio           post-venta, y es por esto, que en 1970 deciden abrir un taller de servicios mecánicos. 

        <br/>
        <br/>
        
        Para el 1992, se inaugura el Departamento de Repuestos, ofreciendo la venta de repuestos, gomas, baterías y aceites. Luego de 30 años de haber fundado la empresa, el señor De Agustino decide vender sus acciones al Sr. Tomás Suárez, momento en el que la empresa se convierte en una empresa familiar.
        
        <br/>
        <br/>

        Actualmente, el yaque motors comercializa las marcas Nissan, Chevrolet y Suzuki, y cuenta con el taller de servicios más capacitado de la región en las marcas mencionadas. En nuestras instalaciones, contamos con una sala de exhibición de vehículos y con un taller de mecánica equipado con las últimas herramientas del mercado, logrando una capacidad de servir a 40 vehículos diarios.

    </p>
    
    
